# Example 1 - For Loops

# This program will show various uses of For in Range loops

# Print numbers from 0 to 9
for i in range(10):
    print(i)

print('\n', '*' * 20, '\n')

# Print numbers from 1 to 10
for i in range(1, 11):
    print(i)

print('\n', '*' * 20, '\n')
    
# Print even numbers from 0 to 10 and increment by 2
for i in range(0, 11, 2):
    print(i)

print('\n', '*' * 20, '\n')
    
# Print odd numbers from 1 to 10 and increment by 2
for i in range(1, 11, 2):
    print(i)